
`use strict`

//......................................................................................................................

onresize = event => M.stage.update(E.state.view)
onclick = event => E.click(event)

//......................................................................................................................

E.start = () =>
{
  M.stage.update(`main`)
}

//......................................................................................................................

E.loop = () =>
{
  E.loops.forEach(loop => loop.run())

  window.requestAnimationFrame(E.loop)
}

//......................................................................................................................

E.start()
E.loop()

