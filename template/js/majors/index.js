
`use strict`

//......................................................................................................................

onresize = () => P.resize()

//......................................................................................................................

P.begin = () =>
{
  const resize = P.resize
  const write = P.write
  const html = P.html
  const get = P.get
  const name = P.state.view
  const block = P.blocks.views[name]
  const screen = get(`#screen`)

  resize()
  write(html(block)).at(screen)
}

//......................................................................................................................

P.loop = () =>
{
  window.requestAnimationFrame(P.loop)
}

//......................................................................................................................

P.begin()
P.loop()

