
`use strict`

//......................................................................................................................

onresize = () => P.resize()

//......................................................................................................................

P.begin = () =>
{
  const {html , get , resize , write} = P

  const name = P.state.view
  const block = P.blocks.views[name]
  const final = html(block)
  const box = get(`#box`)

  resize()

  write(final).at(box)
}

//......................................................................................................................

P.loop = () =>
{
  window.requestAnimationFrame(P.loop)
}

//......................................................................................................................

P.begin()
P.loop()

