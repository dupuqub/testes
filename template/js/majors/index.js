
`use strict`

//......................................................................................................................

onresize = event => E.resize()
onclick = event => E.click(event)
onmousemove = event => E.hover(event)

//......................................................................................................................

E.begin = () =>
{
  E.state.view = `main`

  E.screen.resize()
}


//......................................................................................................................

E.loop = () =>
{
  E.listen.loop()

  window.requestAnimationFrame(E.loop)
}

//......................................................................................................................

E.begin()
E.loop()

