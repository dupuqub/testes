E.screen.reroot = (width , height , unit) =>
{
  //....................................................................................................................

  const newRoot = `--w : ${width}px ; --h : ${height}px ; --u : ${unit}px ;`

  //....................................................................................................................

  const rerooter = sheet =>
  {
    if (sheet.href !== null
    && sheet.href.indexOf(`root.css`) !== - 1)
    {
      sheet.cssRules[0].style.cssText = newRoot

      return true
    }
  }

  //....................................................................................................................

  Array
  .from(document.styleSheets)
  .some(rerooter)

  //....................................................................................................................
}

