
`use strict`

//......................................................................................................................

M.stage.reroot = stage =>
{
  //....................................................................................................................

  const {w , h , unit} = stage
  const newRoot = `--w : ${w}px ; --h : ${h}px ; --u : ${unit}px ;`

  //....................................................................................................................

  const rooter = sheet =>
  {
    if (sheet.href !== null
    && sheet.href.indexOf(`root.css`) !== - 1)
    {
      sheet.cssRules[0].style.cssText = newRoot

      return true
    }
  }

  //....................................................................................................................

  Array
  .from(document.styleSheets)
  .some(rooter)

  //....................................................................................................................
}

